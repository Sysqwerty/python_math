import matplotlib.pyplot as plt
import numpy as np


# –ü–æ–±—É–¥—É—î–º–æ –≥—Ä–∞—Ñ—ñ–∫ —Ñ—É–Ω–∫—Ü—ñ—ó, –¥–ª—è —è–∫–æ–≥–æ –º–∏ –≤–∏–∑–Ω–∞—á–∞—î–º–æ –≥—Ä–∞–Ω–∏—Ü—é. –î–ª—è –ø–æ–±—É–¥–æ–≤–∏ –≥—Ä–∞—Ñ—ñ–∫–∞ –Ω–∞–º –Ω–µ–æ–±—Ö—ñ–¥–Ω–æ –≤–∏–∑–Ω–∞—á–∏—Ç–∏ —ñ–Ω—Ç–µ—Ä–≤–∞–ª, –≤ —è–∫–æ–º—É –Ω–∞ –≥—Ä–∞—Ñ—ñ–∫—É –±—É–¥–µ –∑–º—ñ–Ω—é–≤–∞—Ç–∏—Å—è ùë•
# –í—ñ–∑—å–º–µ–º–æ –∑–º—ñ–Ω–Ω—É ùë• –≤ —ñ–Ω—Ç–µ—Ä–≤–∞–ª—ñ –≤—ñ–¥ -50 –¥–æ 50 : x ‚àà [‚àí50;50]

# Data for plotting
x = np.arange(-50, 50, 0.01)
y = (7*(x**2)+7*x-2)/(x**2-2*x+3)

fig, ax = plt.subplots()
ax.plot(x, y)

ax.set(xlabel='x', ylabel='y',
       title='xy Graph')
ax.grid()

plt.show()
